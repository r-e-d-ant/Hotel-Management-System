    // saving the user button action
    private void saveUserBtnActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_saveUserBtnActionPerformed
        // TODO add your handling code here:
        
        if (userIdBox.getText().isEmpty()
                || fullNameBox.getText().isEmpty()
                || emailBox.getText().isEmpty()
                || passwordBox.getText().isEmpty()) { // #1 validation
            JOptionPane.showMessageDialog(this, "User not created, fill all the fields.");
        } else {
            if (passwordBox.getText().length() >= 6) { // #2 validation
                // Instantiate the User model object
                User theUser = new User();

                // setting the parameters
                theUser.setUserId(userIdBox.getText());
                theUser.setFullName(fullNameBox.getText());
                theUser.setUserEmail(emailBox.getText());
                theUser.setUserPassword(passwordBox.getText());

                // Instantiate the User DAO object
                UserDao userDao = new UserDao();
                
                // invoke register user function from DAO
                int rows = userDao.RegisterUser(theUser);

                if (rows >= 1) {
                    JOptionPane.showMessageDialog(this, "User created");
                    // navigate to login form and close current window
                    Login loginForm = new Login();
                    loginForm.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "User not created");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Password length must be equal or greater than 6");
            }
        }
    }//GEN-LAST:event_saveUserBtnActionPerformed

    // this function is for getting the user level
    // as admin or regular user don't perform same functions
    private String getUserLevel() {
        File file = new File("usrlvl.txt");
        
        Path sourceFile = Paths.get(file.toURI());
        
        try (BufferedReader reader = Files.newBufferedReader(sourceFile)){
            String line = reader.readLine();
            return line;
        } catch (Exception e) {
            System.out.println("Something went wrong! " + e);
            return null;
        }
    }
    
    // check user level for getting a type of user
    // 1 is admin; 0 is regular user;
    private void gotoMenuLinkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gotoMenuLinkMouseClicked
        // TODO add your handling code here:
        if ((getUserLevel()).equals("1")) {
            Admin adminForm = new Admin();
            adminForm.setVisible(true);
            this.dispose();
        } else {
            Receptionist recepForm = new Receptionist();
            recepForm.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_gotoMenuLinkMouseClicked

